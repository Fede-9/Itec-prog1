# Operadores de Asignacion

edad = 5

print(edad)

# Mas igual
print('------ MAS IGUAL -------')

edad = 5
edad += 2 # edad = edad + 2

print(edad)

# Menos igual
print('------ MENOS IGUAL -------')

edad = 5
edad -= 2 # edad = edad - 2

print(edad)

# Multiplicacion igual
print('------ MULTIPLICACION IGUAL -------')

edad = 5
edad *= 2 # edad = edad * 2

print(edad)

# Division igual
print('------ DIVISION IGUAL -------')

edad = 5
edad /= 2 # edad = edad / 2

print(edad)

# Floor Division igual
print('------ FLOOR DIVISION IGUAL -------')

edad = 5
edad //= 2 # edad = edad // 2

print(edad)

# Modulo igual
print('------ MODULO IGUAL -------')

edad = 5 
edad %= 2 # edad = edad % 2 --> 2 * 2 = 4 --> 5 - 4 = 1 ( RESTO DE LA DIVISION )

print(edad)