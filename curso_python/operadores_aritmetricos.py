# Operadores Aritmeticos

# Suma
print(5 + 5)
print(5.10 + 10.30)
print(5 + 3.5)

# Resta
print('--------RESTA-----')
print(10 - 3)
print(12 - 4.5)
print(10.5 - 5.5)

# Multiplicacion
print('--------MULTIPLICACION-----')
print(3 * 5)
print(2 * 4)
print(3.5 * 2.5)

# Division
print('--------DIVISION-----')
print(8 / 4)
print(10 / 2.5)
print(10.5 / 2.5)

# Floor Division : nos da la parte entera de la division
print('--------FLOOR DIVISION-----')  
print(10 // 4)
print(15 // 4)
print(15 / 4)

# Modulo
print('-------- MODULO -----')
print(11 % 4) # 4 * 2 = 8 ---> 11 - 8 = 3 ( QUE SERIA EL RESTO DE LA DIVISION )
print(12 % 4) # 4 * 3 = 12 ---> NO TENEMOS RESTO