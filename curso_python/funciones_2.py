# Funciones que devuelven un valor


def multiplicar_por_2(numero):
    
    resultado = numero * 2
    return resultado

print(multiplicar_por_2(4))   #se puede poner de distintas formas.
mi_resultado_1 = multiplicar_por_2(numero = 10) 
print(mi_resultado_1)

print('-----------------------------')

def sumar_1000(numero):
    
    resultado = numero + 1000
    return resultado


mi_resultado_2 = sumar_1000(numero = 500) 
print(mi_resultado_2)

