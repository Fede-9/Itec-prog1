# Operadores Comparativos o Relacionales

# Mayor
print('------Operador Mayor-------')
print(10 > 5)
print(10 > 200)

# Menor
print('------Operador Menor-------')
print(10 < 5)
print(10 < 200)

# Igualdad
print('------Igualdad-------')
print(10 == 10)
print(10 == 3)

# Mayor o igual
print('------ MAYOR O IGUAL -------')
print(10 >= 10)
print(15 >= 10)
print(8 >= 10)

# Menor o igual
print('------ MENOR O IGUAL -------')
print(10 <= 10)
print(15 <= 10)
print(8 <= 10)

# Distinto
print('--------DISTINTO-------')
print(10 != 10)
print(10 != 5)


