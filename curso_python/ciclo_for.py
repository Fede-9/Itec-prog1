# Ciclo For


numeros = [1,2,3,4]

for numero in numeros:
    print(numero)


# 1era ejecucion del ciclo for, 'numero' es equivalente a --> numeros[0] ---> 1
# 2enda ejecucion del ciclo for, 'numero' es equivalente a --> numeros[1] ---> 2
# 3era ejecucion del ciclo for, 'numero' es equivalente a --> numeros[2] ---> 3
# 4ta ejecucion del ciclo for, 'numero' es equivalente a --> numeros[3] ---> 4



